package org.example.function.a3Parametre2;

class ComparisonData {
    private int first;
    private int second;
    private String comparator;

    public ComparisonData(int first, int second, String comparator) {
        this.first = first;
        this.second = second;
        this.comparator = comparator;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }

    public String getComparator() {
        return comparator;
    }
}
